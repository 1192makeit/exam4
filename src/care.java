public class care {
    private String name;
    private String temperture;
    private int price;

    public care(String name, String temperture, int price) {
        this.name = name;
        this.temperture = temperture;
        this.price = price;
    }

    public String getname() {
        return name;
    }

    public String gettemperture() {
        return temperture;
    }

    public int getprice() {
        return price;
    }
}

